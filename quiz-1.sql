-- 급여가 15000이상인 직원 조회

SELECT *
FROM EMPLOYEES
WHERE SALARY >= 15000;

-- 입사년도가 2005년인 직원 조회
SELECT *
FROM EMPLOYEES
WHERE HIRE_DATE BETWEEN '2005-01-01' AND '2005-12-31';

-- 부서이름이 IT인 직원 조회
SELECT *
FROM EMPLOYEES
WHERE DEPARTMENT_ID = 60; -- DEPARTMENT 테이블을 조회하면 IT = 60

-- 전화번호가 590으로 시작하는 직원 조회
SELECT *
FROM EMPLOYEES
WHERE PHONE_NUMBER LIKE '590%';

